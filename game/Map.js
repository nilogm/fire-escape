    setFog(scale=4){
        this.vision = this.scene.make.image({
            x: 0,
            y: 0,
            key: 'fog',
            add: false
        })
        this.vision.scale = scale
        this.rt.mask = new Phaser.Display.Masks.BitmapMask(this.scene, this.vision)
        this.rt.mask.invertAlpha = true
    }

    /**
     * 
     * @param {Player} player 
     */
    updateFogPosition(player){
        if (this.vision)
        {
            this.vision.x = player.obj.x + 4
            this.vision.y = player.obj.y + 2
        }
    }

    /**
     * 
     * @param {TileGenerator} tiles 
     * @param {int} amount 
     */
    generateEnvironment(group, player, tiles, amount, min=[0,0], max=[0,0]){
        this.createdEnvironment = false
        this.scene.physics.add.overlap(group, group, (obj1, obj2)=>{
            if (this.createdEnvironment == false){
                var pos = this.getPosition(min, max, 64)
                obj2.setPosition(pos[0], pos[1])
            }
        })
        this.scene.physics.add.overlap(player, group, (obj1, obj2)=>{
            if (this.createdEnvironment == false){
                var pos = this.getPosition(min, max, 64)
                obj2.setPosition(pos[0], pos[1])
            }
        })

        var items = ['water', 'table', 'large_table', 'drawer', 'chair']
        for (let a = 0; a < amount; a++) {
            var type = Phaser.Math.Between(0, 4)
            var pos = this.getPosition(min, max, 64)
            var env = tiles.setEnvironment(group, items[type], pos[0], pos[1])
        }

        this.createdEnvironment = true
        this.scene.physics.add.collider(group, group)
        this.scene.physics.add.collider(group, player.obj)
    }

    generateDecorations(tiles, amount, min=[0,0], max=[0,0]){
        for (let a = 0; a < amount; a++) {
            var pos = this.getPosition(min, max, 64)
            tiles.setDecorations(pos[0], pos[1])
        }
    }

    getPosition(min=[0, 0], max=[800, 600], offset=0){
        return [Phaser.Math.Between(min[0] + offset, max[0] - offset), Phaser.Math.Between(min[1] + offset, max[1] - offset)]
    }
